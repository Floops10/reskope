/* Retire les accents et passe en capitales pour le rendu graphique.
   Le texte réel (accessible / SEO) reste intact ailleurs. */
function toGlyphKeys(text) {
  return text
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .toUpperCase();
}

/* Calcule la disposition absolue d'un mot : nœuds + liens positionnés,
   et la boîte (largeur/hauteur) pour le viewBox. */
export function layoutWord(text) {
  const keys = toGlyphKeys(text);
  const nodes = [];
  const links = [];
  let x = 0;

  for (const ch of keys) {
    if (ch === ' ') {
      x += SPACE + TRACK;
      continue;
    }
    const g = GLYPHS[ch];
    if (!g) {
      x += SPACE + TRACK;
      continue;
    }
    const base = nodes.length;
    g.n.forEach(([nx, ny]) => nodes.push([nx + x, ny]));
    g.l.forEach(([a, b]) => links.push([base + a, base + b]));
    x += g.w + TRACK;
  }

  const width = Math.max(x - TRACK, 1);
  return { nodes, links, width, height: CAP };
}
